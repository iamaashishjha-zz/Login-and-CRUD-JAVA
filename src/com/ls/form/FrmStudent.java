    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            if (txtSid.getText().equals("")
                    || txtStudentName.getText().equals("")
                    || txtStudentAddress.getText().equals("")
                    || cmbCourse.getSelectedItem().toString().equals("")) {
                JOptionPane.showMessageDialog(null, "Must fill all fields");
            } else {
                Student s = new Student();
                StudentDao sd = new StudentDaoImpl();

                int SID = Integer.parseInt(txtSid.getText());
                s.setSID(SID);
                s.setsName(txtStudentName.getText());
                s.setsAddress(txtStudentAddress.getText());
                s.setsCourse(cmbCourse.getSelectedItem().toString());

                sd.saveStudent(s);

                JOptionPane.showMessageDialog(null, "Record created successfully");

                txtSid.setText("");
                txtStudentName.setText("");
                txtStudentAddress.setText("");
                cmbCourse.setSelectedIndex(0);

                loadData();

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (txtSid.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Must fill SID field");
        } else {
            try {
                Student s = new Student();
                StudentDao sd = new StudentDaoImpl();
                int SID = Integer.parseInt(txtSid.getText());
                s.setSID(SID);
                sd.deleteStudent(SID);

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
            JOptionPane.showMessageDialog(null, "Record deleted successfully");
            txtSid.setText("");
            loadData();

        }

    }//GEN-LAST:event_btnDeleteActionPerformed

