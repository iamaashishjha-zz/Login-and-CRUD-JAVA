    private void loadData() {
        try {
            ArrayList<String> fileData;
            StudentDao sd = new StudentDaoImpl();
            fileData = sd.getAlLStudents();
            DefaultTableModel dtm = new DefaultTableModel(new String[]{"SID", "Name", "Address", "Course"}, 0);
            Object[] tableLines = fileData.toArray();
            for (int i = 0; i < tableLines.length; i++) {
                String lines = tableLines[i].toString();
                String[] row = lines.split(",");
                dtm.addRow(row);

            }
            jTable1.setModel(dtm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    private void dataBinding() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int rowIndex = jTable1.getSelectedRow();
        txtSid.setText(dtm.getValueAt(rowIndex, 0).toString());
        txtStudentName.setText(dtm.getValueAt(rowIndex, 1).toString());
        txtStudentAddress.setText(dtm.getValueAt(rowIndex, 2).toString());
        cmbCourse.setSelectedItem(dtm.getValueAt(rowIndex, 3));
    }
